-- CONDITIONAL STATEMENTS
DECLARE
	NUM NUMBER := 1;
BEGIN
	IF MOD(NUM,2)=0 THEN
		DBMS_OUTPUT.PUT_LINE(NUM||' IS AN EVEN NUMBER');
	ELSE
		DBMS_OUTPUT.PUT_LINE(NUM||' IS AN ODD NUMBER');
	END IF;
END;

