DECLARE
	NUM NUMBER := 70;
	CNT NUMBER := 0;
BEGIN
	FOR I IN 2..NUM/2 LOOP
		IF MOD(NUM,I)=0 THEN
			CNT := 1;
			EXIT;
		END IF;
	END LOOP;
	IF CNT=0 THEN
		DBMS_OUTPUT.PUT_LINE(NUM||' IS A PRIME NUMBER');
	ELSE
		DBMS_OUTPUT.PUT_LINE(NUM||' IS NOT A PRIME NUMBER');
	END IF;
END;

DECLARE
	ROW NUMBER := 5;
	SPC NUMBER := ROW-1;
BEGIN
	FOR I IN 1..ROW LOOP
		FOR J IN 1..SPC LOOP
			DBMS_OUTPUT.PUT(' ');
		END LOOP;
		FOR K IN 1..I LOOP
			DBMS_OUTPUT.PUT('* ');
		END LOOP;
		DBMS_OUTPUT.PUT_LINE('');
		SPC := SPC - 1;
	END LOOP;
END;

--EXPLICIT CURSOR
DECLARE
FNAME EMPLOYEES.FIRST_NAME%TYPE;
LNAME EMPLOYEES.LAST_NAME%TYPE;
SAL EMPLOYEES.SALARY%TYPE;
DID EMPLOYEES.DEPARTMENT_ID%TYPE;
MID EMPLOYEES.MANAGER_ID%TYPE;
CURSOR EMP_DETAIL IS SELECT FIRST_NAME,LAST_NAME,SALARY,DEPARTMENT_ID,MANAGER_ID FROM EMPLOYEES;
BEGIN
    OPEN EMP_DETAIL;
	LOOP
		FETCH EMP_DETAIL INTO FNAME,LNAME,SAL,DID,MID;
		DBMS_OUTPUT.PUT_LINE(FNAME||' '||LNAME||"'S MONTHLY SALARY IS "||SAL||' WHO WORKS IN DEPARTMENT '||DID||' UNDER MANAGER '||MID);
		IF EMP_DETAIL%NOTFOUND THEN
			EXIT;
		END IF;
	END LOOP;
	CLOSE EMP_DETAIL;
END;

