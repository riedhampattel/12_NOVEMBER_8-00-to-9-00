#include<stdio.h>
int main()
{
	int i;
	printf("\nFor loop := ");
	for(i=11;i<=5;i++)
	{
		printf("\nTops Technologies");//5
	}
	
	printf("\n\nWhile loop := ");
	i=11;
	while(i<=5)
	{
		printf("\nTops Technologies");
		i++;
	}
	
	printf("\n\nDo while loop := ");
	i=11;
	do
	{
		printf("\nTops Technologies");
		i++;
	}while(i<=5);
	return 0;
}
